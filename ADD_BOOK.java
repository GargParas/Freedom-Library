    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jButton4.setEnabled(true);       
        jButton3.setEnabled(false);    
        int acc=0;
        String isbn =jTextField2.getText();
        String title = jTextField3.getText();
        String author = jTextField4.getText();
        String publisher = jTextField5.getText();
        int quant =Integer.parseInt(jTextField6.getText());
        int price =Integer.parseInt(jTextField8.getText());
        int pages =Integer.parseInt(jTextField9.getText());

        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/lib","root","PARAM");
            st=con.createStatement();
            int nquant=quant;
            while (quant>0)
        {
            st.execute("insert into addbook values ("+"'"+acc+"'"+","+"'"+isbn+"'"+","+"'"+title+"'"+","+"'"+author+"'"+","+"'"+publisher+"'"+","+"'"+price+"'"+","+"'"+pages+"'"+ ")");
            st.execute("insert into book_info values ("+"'"+acc+"'"+","+"'"+isbn+"'"+","+"'"+title+"'"+","+"'"+author+"'"+","+"'"+publisher+"'"+","+"'"+price+"'"+","+"'"+pages+"'"+ ")");
            quant--;
        }
            rs= st.executeQuery("select * from addbook ORDER BY acc_no DESC LIMIT 1");
           
            rs.next();
            String msg="";
            int l=Integer.parseInt(rs.getString(1))-nquant;
            
            for(int i=Integer.parseInt(rs.getString(1));i>l;i--)
            {
                msg=msg+" , "+i;
            }
            jLabel12.setText(msg);
            JOptionPane.showMessageDialog(null,"THE ACC NO FOR THE BOOK IS "+msg+" and BOOK IS ADDED SUCCESSFULLY");
        }
        catch(Exception e)
        {
           e.printStackTrace();
           JOptionPane.showMessageDialog(this,"  CHECK THE INFORMATION",null,JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

